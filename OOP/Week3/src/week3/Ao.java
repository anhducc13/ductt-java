package week3;

public class Ao {
    private String mauAo;
    private String sizeAo;
    private String loaiAO;

    public String getMauAo() {
        return mauAo;
    }

    public void setMauAo(String mauAo) {
        this.mauAo = mauAo;
    }

    public String getSizeAo() {
        return sizeAo;
    }

    public void setSizeAo(String sizeAo) {
        this.sizeAo = sizeAo;
    }

    public String getLoaiAO() {
        return loaiAO;
    }

    public void setLoaiAO(String loaiAO) {
        this.loaiAO = loaiAO;
    }
}
