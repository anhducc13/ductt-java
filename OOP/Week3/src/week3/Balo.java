package week3;

public class Balo {
    private String hang;
    private int soNgan;
    private String mau;

    public String getHang() {
        return hang;
    }

    public void setHang(String hang) {
        this.hang = hang;
    }

    public int getSoNgan() {
        return soNgan;
    }

    public void setSoNgan(int soNgan) {
        this.soNgan = soNgan;
    }

    public String getMau() {
        return mau;
    }

    public void setMau(String mau) {
        this.mau = mau;
    }
    
}
